SCRIPT_NAME=$(basename "${0}")
SCRIPT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" &> /dev/null && pwd )"

###############################################################################
# IMPORT
###############################################################################

set -a
source "${SCRIPT_DIR}/../.env"
set +a

###############################################################################
# FUNCTIONS
###############################################################################

function nsout () {
  NAMESPACE="${1}"
  NAMESPACE="${SCRIPT_NAME}"
  exec > >(sed "s/^/${NAMESPACE} (1) > /")
  exec 2> >(sed "s/^/${NAMESPACE} (2) > /" >&2)
}

